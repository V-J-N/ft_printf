#include "ft_printf.h"

int	len_int_nbr(int n)
{
	int	i;

	if (n == 0)
		return (1);
	if (n >= 0)
		i = 0;
	else
	{
		i = 1;
		n *= -1;
	}
	while (n != 0)
	{
		n = n / 10;
		i++;
	}
	return (i);
}
